export const getRegionsByCountry = (country) => {
  switch (country) {
    case "France":
      return FRENCH_REGIONS;
    case "Italie":
      return ITALIAN_REGIONS;
    case "Espagne":
      return SPANISH_REGIONS;
    default:
      return OTHER_REGIONS.filter((region) =>
        region.label.includes(`(${country})`)
      );
  }
};

